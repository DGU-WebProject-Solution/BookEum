package dao.bean;

public class Userbean {
    private int id;
    private String name;
    private String birth;
    private String addressCity;
    private String addressGu;
    private String password;
    private String giveBook;
    private String getBook;
    private String emailId;
    private int peopleCnt;
    private int newRate;
    private double avgRate;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getAddressCity() {
		return addressCity;
	}
	public void setAddressCity(String addressCity) {
		this.addressCity = addressCity;
	}
	public String getAddressGu() {
		return addressGu;
	}
	public void setAddressGu(String addressGu) {
		this.addressGu = addressGu;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getGiveBook() {
		return giveBook;
	}
	public void setGiveBook(String giveBook) {
		this.giveBook = giveBook;
	}
	public String getGetBook() {
		return getBook;
	}
	public void setGetBook(String getBook) {
		this.getBook = getBook;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public int getPeopleCnt() {
		return peopleCnt;
	}
	public void setPeopleCnt(int peopleCnt) {
		this.peopleCnt = peopleCnt;
	}
	public int getNewRate() {
		return newRate;
	}
	public void setNewRate(int newRate) {
		this.newRate = newRate;
	}
	public double getAvgRate() {
		return avgRate;
	}
	public void setAvgRate(double avgRate) {
		this.avgRate = avgRate;
	}

   
}
